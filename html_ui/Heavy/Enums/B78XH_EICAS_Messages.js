/**
 * Heavy Division EICAS Messages auxiliary classes
 */

class EicasMessage {

	get id() {
		return this._id;
	}

	get message() {
		return this._message;
	}

	constructor(id) {
		this._id = id;
		this._message = EICASMessages[this._id];
		this._level = Math.floor(this._id / 1000);
	}

	level(id = false) {
		return (id ? this._level : EICASMessageLevel[this._level]);
	}

	toString() {
		return this._message;
	}
}

